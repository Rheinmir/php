<?php
//switch case
$day = 8;
switch ($day) {
    case 2:
        echo "Today is Mon";
        break;
    case 3:
        echo "Today is Tue";
        break;
    case 4:
        echo "Today is Wed";
        break;
    case 5:
        echo "Today is Thu";
        break;
    case 6:
        echo "Today is Fri";
        break;
    case 7:
        echo "Today is Sat";
        break;
    default:
        echo "Today is Sun";
        break;
}
